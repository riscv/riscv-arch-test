import sys
from pathlib import Path
from textwrap import dedent


def makePrivHeader(testname: str, sigupd_count: int, extra_reqext: str, extra_march: str) -> str:
    """Generate the header for privileged tests."""
    cmd = " ".join(sys.argv)
    more_reqext = ""
    if extra_reqext:
        more_reqext = ", " + extra_reqext
    more_march = ""
    if extra_march:
        more_march = "_" + extra_march
    # TODO: it would be nice to use e as the base for priv tests to ensure they run everywhere, but Makefiles would need to be updated to emit mabi=ilp32e/lp64e
    march_base = "rv${XLEN}i_zicsr"
    header_lines = dedent(f"""\
        ///////////////////////////////////////////
        // {testname}.S
        //
        // Autogenerated by {cmd}
        // Do not modify manually
        //
        // SPDX-License-Identifier: Apache-2.0
        ///////////////////////////////////////////

        ##### START_TEST_CONFIG #####
        # REQUIRED_EXTENSIONS: [I, Zicsr, Sm{more_reqext}]
        # MARCH: {march_base}{more_march}
        # CONFIG_DEPENDENT: true
        ##### END_TEST_CONFIG #####

        #define TEST_FILE "{testname}"
        #define SIGUPD_COUNT {sigupd_count}  // figure out count
        #include "riscv_arch_test.h"
        .option norvc          # Do not automatically replace uncompressed instructions with compressed instructions

        // Set up test environment and declare main entry point
        RVTEST_BEGIN

        """)
    return header_lines


def makePrivFooter(basename: str, testcase_lines: str) -> str:
    """Generate the footer for privileged tests."""
    footer_lines = dedent(f"""\
        // Instantiate trap handlers and other epilog code, call RVMODEL_HALT at end of test
        RVTEST_CODE_END

        // Create scratch data sections and label for test data
        RVTEST_DATA_BEGIN

        // Testcase strings
        canary_mismatch: .string "Testcase signature canary mismatch!"
        {testcase_lines}
        #test_1: .string "\"test: 1; cp: {basename}/cp_rs1\""

        // End of test data section and model specific data
        RVTEST_DATA_END

        // Signature region for self-checking
        RVTEST_SIG_SETUP
        """)
    return footer_lines


def addCSRWriteTest(csr_name: str, reg: str, coverpoint: str, cp_bin: str) -> None:
    """Add a CSR write test.  Update global variables, including the signature update count, body lines, and testcase lines."""
    global sigupd_count, body_lines, testcase_lines
    sigupd_count += 1
    body_lines += f"\tCSR_WRITE({csr_name}, {reg}, {reg}, DEFAULT_SIG_PTR, DEFAULT_LINK_REG, DEFAULT_TEMP_REG, test_{sigupd_count})\n"
    testcase_lines += f'test_{sigupd_count}: .string "\\"test: {sigupd_count}; cp: {coverpoint}_{cp_bin}\\""\n'


def makePrivBody() -> None:
    global body_lines
    """Generate the body for privileged tests.  Also updates the signature count and testcase messages"""

    body_lines = dedent("""\
        /////////////////////////////////
        // cp_mcause_write_exception
        /////////////////////////////////
            csrr s0, mcause     # save CSR
            // with interrupt = 0: walking 1s in fields except reserved and custom
        """)

    causes = (i for i in range(0, 24) if i not in {14, 17})  # skip reserved and custom causes
    coverpoint = "ZicsrM_mcause_cg_cp_mcause_write_exception"
    for i in causes:
        body_lines += f"\n{coverpoint}_{i}:\n"
        body_lines += f"\tli t0, {i}           # exception cause {i}\n"
        addCSRWriteTest("mcause", "t0", coverpoint, i)

    body_lines += dedent("""\
        /////////////////////////////////
        // cp_mcause_write_interrupt
        /////////////////////////////////
            // with interrupt = 1: walking 1s in fields except reserved and custom
        """)

    body_lines += "\tSET_MSB(a0)  # set a0 to have msb = 1 for interrupt tests\n"
    causes = (i for i in range(0, 14) if i not in {0, 4, 8})  # skip reserved and custom causes
    coverpoint = "ZicsrM_mcause_cg_cp_mcause_write_interrupt"
    for i in causes:
        body_lines += f"\n{coverpoint}_{i}:\n"
        body_lines += f"\tli t0, {i}           # interrupt cause {i}\n"
        body_lines += "\tor t0, t0, a0        # set interrupt bit\n"
        addCSRWriteTest("mcause", "t0", coverpoint, i)

    body_lines += "csrw mcause, s0    # restore CSR"


# Global variables
sigupd_count = 0  # keep track of size of signature
body_lines = ""  # body of test
testcase_lines = ""  # testcase strings for reporting mismatches

# Main Function

basename = "ZicsrM"
fname = f"{basename}-01.S"
pathname = f"./{fname}"
with Path.open(pathname, "w") as outfile:
    sys.stdout = outfile

    makePrivBody()  # populate body_lines and testcase_lines
    header_lines = makePrivHeader(fname, sigupd_count, "", "")
    footer_lines = makePrivFooter(basename, testcase_lines)

    print(header_lines)
    print(body_lines)
    print(footer_lines)
