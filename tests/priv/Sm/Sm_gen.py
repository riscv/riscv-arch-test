import sys
from pathlib import Path
from textwrap import dedent


def makePrivHeader(testname: str, sigupd_count: int, extra_reqext: str, extra_march: str) -> str:
    """Generate the header for privileged tests."""
    cmd = " ".join(sys.argv)
    more_reqext = ""
    if extra_reqext:
        more_reqext = ", " + extra_reqext
    more_march = ""
    if extra_march:
        more_march = "_" + extra_march
    # TODO: it would be nice to use e as the base for priv tests to ensure they run everywhere, but Makefiles would need to be updated to emit mabi=ilp32e/lp64e
    march_base = "rv${XLEN}i_zicsr"
    header_lines = dedent(f"""\
        ///////////////////////////////////////////
        // {testname}
        //
        // Autogenerated by {cmd}
        // Do not modify manually
        //
        // SPDX-License-Identifier: Apache-2.0
        ///////////////////////////////////////////

        ##### START_TEST_CONFIG #####
        # REQUIRED_EXTENSIONS: [I, Zicsr, Sm{more_reqext}]
        # MARCH: {march_base}{more_march}
        # CONFIG_DEPENDENT: true
        ##### END_TEST_CONFIG #####

        # dh 1/1/26 *** will need MSECCFG_SUPPORTED or some equivalent defined in UDB and used here.
        # See https://github.com/riscv-software-src/riscv-unified-db/issues/1293

        // Define the name of this test file for reporting and the size of the signature region
        #define TEST_FILE "{testname}"
        #define SIGUPD_COUNT {sigupd_count}

        // Enable M-mode trap handler
        #define rvtest_mtrap_routine

        // Include framework macros
        #include "riscv_arch_test.h"

        // Set up test environment and declare main entry point
        RVTEST_BEGIN
        .option norvc          // Do not automatically replace uncompressed instructions with compressed instructions
        """)
    return header_lines


def makePrivFooter(basename: str, testcase_lines: str) -> str:
    """Generate the footer for privileged tests."""
    footer_lines = dedent("""
        /////////////////////////////////
        // Instantiate trap handlers and other epilog code, call RVMODEL_HALT at end of test
        /////////////////////////////////

        RVTEST_CODE_END

        /////////////////////////////////
        // Labels for testcases used in reporting
        /////////////////////////////////

        RVTEST_DATA_BEGIN

        """)
    footer_lines += f"{testcase_lines}"
    footer_lines += dedent("""
        RVTEST_DATA_END

        /////////////////////////////////
        // Signature region for self-checking
        /////////////////////////////////

        RVTEST_SIG_SETUP
        """)
    return footer_lines


def addCSRReadTest(csr_name: str, reg: str, cpbin: str) -> None:
    """Add a CSR read test.  Update global variables, including the signature update count, body lines, and testcase lines."""
    global sigupd_count, body_lines, testcase_lines
    sigupd_count += 1
    body_lines += f"\tRVTEST_SIGUPD_CSR_READ({csr_name}, {reg}, test_{sigupd_count}) # Read {csr_name} into {reg} and check against expected\n"
    testcase_lines += f'test_{sigupd_count}: .string "\\"test: {sigupd_count}; cp: {cpbin}\\""\n'


def addCSRWriteTest(csr_name: str, reg: str, cpbin: str) -> None:
    """Add a CSR write test.  Update global variables, including the signature update count, body lines, and testcase lines."""
    global sigupd_count, body_lines, testcase_lines
    sigupd_count += 1
    body_lines += f"\tRVTEST_SIGUPD_CSR_WRITE({csr_name}, {reg}, test_{sigupd_count}) # Write {reg} to {csr_name}, then read back to {reg} and check against expected\n"
    testcase_lines += f'test_{sigupd_count}: .string "\\"test: {sigupd_count}; cp: {cpbin}\\""\n'


def addSignature(reg: str, cpbin: str) -> None:
    """Add a signature read.  Update global variables, including the signature update count, body lines, and testcase lines."""
    global sigupd_count, body_lines, testcase_lines
    sigupd_count += 1
    body_lines += "\t// Can't use DEFAULT_*_REG macros here because of macro expansion order\n"
    body_lines += "\t// DEFAULT_SIG_REG = x2, DEFAULT_TEMP_REG = x4, DEFAULT_LINK_REG = x5\n"
    body_lines += f"\tRVTEST_SIGUPD(x2, x5, x4, {reg}, test_{sigupd_count}) # Check {reg} against expected value\n"
    testcase_lines += f'test_{sigupd_count}: .string "\\"test: {sigupd_count}; cp: {cpbin}\\""\n'


def addAccessTest(csr: str, covergroup: str, coverpoint: str) -> None:
    """Add a CSR access test for the given CSR."""
    global body_lines

    body_lines += f"\n{covergroup}_{coverpoint}_{csr}:\n"
    body_lines += f"\tCSRR(s0, {csr})    # Save CSR\n"
    body_lines += "\tli t1, -1           # t1 = all 1s\n"
    body_lines += f"\tCSRW({csr}, t1)    # Write all 1s to CSR\n"
    addCSRReadTest(csr, "t0", f"{coverpoint}/{coverpoint}/{csr}_writeall1s")
    body_lines += f"\tCSRW({csr}, zero)   # Write all 0s to CSR\n"
    addCSRReadTest(csr, "t0", f"{coverpoint}/{coverpoint}/{csr}_writeall0s")
    body_lines += f"\tCSRS({csr}, t1)    # Set all CSR bits\n"
    addCSRReadTest(csr, "t0", f"{coverpoint}/{coverpoint}/{csr}_setall")
    body_lines += f"\tCSRC({csr}, t1)    # Clear all CSR bits\n"
    addCSRReadTest(csr, "t0", f"{coverpoint}/{coverpoint}/{csr}_clearall")
    body_lines += f"\tCSRW({csr}, s0)       # Restore CSR\n"


def addWalkTest(csr: str, covergroup: str, coverpoint: str) -> None:
    """Add a CSR walking ones test for the given CSR."""
    global body_lines

    body_lines += f"\n{covergroup}_{coverpoint}_{csr}:\n"
    body_lines += f"\tCSRR(s0, {csr})      # Save CSR\n"
    body_lines += "\tli t1, -1             # t1 = all 1s\n"
    body_lines += "\tli a0, 1            # 1 in lsb\n"
    for i in range(0, 32):
        body_lines += f"\n{covergroup}_{coverpoint}_{csr}_set_bit_{i}:\n"
        body_lines += f"\tCSRW({csr}, zero)    # clear all bits\n"
        body_lines += f"\tCSRS({csr}, a0)      # set walking 1 in column {i}\n"
        addCSRReadTest(csr, "t0", f"{coverpoint}/{coverpoint}/{csr}_set_bit_{i}")
        body_lines += "\tslli a0, a0, 1      # walk the 1\n"
    body_lines += "\n#if __riscv_xlen == 64\n"
    for i in range(32, 64):
        body_lines += f"\n{covergroup}_{coverpoint}_{csr}_set_bit_{i}:\n"
        body_lines += f"\t\tCSRW({csr}, zero)    # clear all bits\n"
        body_lines += f"\t\tCSRS({csr}, a0)      # set walking 1 in column {i}\n"
        addCSRReadTest(csr, "t0", f"{coverpoint}/{coverpoint}/{csr}_set_bit_{i}")
        body_lines += "\t\tslli a0, a0, 1      # walk the 1\n"
    body_lines += "#endif\n"
    body_lines += "\tli a0, 1            # 1 in lsb\n"
    for i in range(0, 32):
        body_lines += f"\n{covergroup}_{coverpoint}_{csr}_clr_bit_{i}:\n"
        body_lines += f"\tCSRW({csr}, t1)      # set all bits\n"
        body_lines += f"\tCSRC({csr}, a0)      # clear walking 1 in column {i}\n"
        addCSRReadTest(csr, "t0", f"{coverpoint}/{coverpoint}/{csr}_clr_bit_{i}")
        body_lines += "\tslli a0, a0, 1      # walk the 1\n"
    body_lines += "\n#if __riscv_xlen == 64\n"
    for i in range(32, 64):
        body_lines += f"\n{covergroup}_{coverpoint}_{csr}_clr_bit_{i}:\n"
        body_lines += f"\t\tCSRW({csr}, t1)    # set all bits\n"
        body_lines += f"\t\tCSRC({csr}, a0)      # clear walking 1 in column {i}\n"
        addCSRReadTest(csr, "t0", f"{coverpoint}/{coverpoint}/{csr}_clr_bit_{i}")
        body_lines += "\t\tslli a0, a0, 1      # walk the 1\n"
    body_lines += "#endif\n"
    body_lines += f"\tCSRW({csr}, s0)            # restore CSR\n"


def makePrivBody() -> None:
    global body_lines
    """Populates the global body for privileged tests.  Also updates the global signature count and testcase messages"""

    covergroup = "Sm_mcause_cg"

    body_lines = dedent("""
            CSRR(s0, mcause)     # save CSR before testing it

        /////////////////////////////////
        // cp_mcause_write_exception
        //   with interrupt = 0: test writing each exception cause
        /////////////////////////////////
        """)

    causes = (i for i in range(0, 24) if i not in {14, 17})  # skip reserved causes
    coverpoint = "cp_mcause_write_exception"
    for i in causes:
        binname = f"b_{i}"
        body_lines += f"\n{covergroup}_{coverpoint}_{binname}:\n"
        body_lines += f"\tLI(t0, {i})           # exception cause {i}\n"
        addCSRWriteTest("mcause", "t0", f"{coverpoint}/{coverpoint}/{binname}")

    body_lines += dedent("""
        /////////////////////////////////
        // cp_mcause_write_interrupt
        //   with interrupt = 1: test writing each interrupt cause
        /////////////////////////////////
        """)

    body_lines += "\tSET_MSB(a0)  # set a0 to have msb = 1 for interrupt tests\n"
    causes = (i for i in range(0, 14) if i not in {0, 4, 8})  # skip reserved causes

    coverpoint = "cp_mcause_write_interrupt"
    for i in causes:
        binname = f"b_{i}"
        body_lines += f"\n{covergroup}_{coverpoint}_{binname}:\n"
        body_lines += f"\tLI(t0, {i})           # interrupt cause {i}\n"
        body_lines += "\tor t0, t0, a0          # set interrupt bit\n"
        addCSRWriteTest("mcause", "t0", f"{coverpoint}/{coverpoint}/{binname}")

    body_lines += "\n\tCSRW(mcause, s0)       # restore CSR\n"

    covergroup = "Sm_mstatus_cg"
    coverpoint = "cp_mstatus_sd_write"

    body_lines += dedent("""
        /////////////////////////////////
        // cp_mstatus_sd_write
        //   Write all combinations of mstatus.SD = {0/1}, FS/XS/VS = {00, 01, 10, 11}
        //   mstatus.SD is read-only, so nothing should happen
        /////////////////////////////////

            SET_MSB(a0) # put a 1 in the msb of a0 (XLEN-1)
            CSRR(s0, mstatus)        # read and save mstatus
            # set up a1 with mstatus except SD, FS, XS, VS cleared
            not t0, a0              # t0 has all but msb set
            and a1, s0, t0          # clear SD bit
            LI(t0, 0x1E600)         # t0 has all FS, XS, VS bits set (bits [14:13], [16:15], [10:9], respectively)
            not t0, t0              # t0 has all but FS, XS, VS bits set
            and a1, a1, t0          # clear FS, XS, VS bits
        """)

    for sd in (0, 1):
        for fs in (0, 1, 2, 3):
            for xs in (0, 1, 2, 3):
                for vs in (0, 1, 2, 3):
                    binname = f"sd_{sd}_fs_{fs:02b}_xs_{xs:02b}_vs_{vs:02b}"
                    body_lines += f"\n{covergroup}_{coverpoint}_{binname}:\n"
                    fields = fs << 13 | xs << 15 | vs << 9
                    body_lines += f"\tLI(t0, 0x{fields:08x})  # fs = {fs:02b} xs = {xs:02b} vs = {vs:02b}\n"
                    if sd == 1:
                        body_lines += "\tor t0, t0, a0      # set SD bit\n"
                    body_lines += (
                        "\tor t0, t0, a1          # value to write to mstatus with SD/FS/XS/VS bits set/clear\n"
                    )
                    addCSRWriteTest("mstatus", "t0", f"{coverpoint}/{coverpoint}/{binname}")

    body_lines += "\n\tCSRW(mstatus, s0)    # restore CSR\n"

    covergroup = "Sm_mprivinst_cg"
    coverpoint = "cp_mprvinst"

    body_lines += dedent("""
        /////////////////////////////////
        // cp_mprvinst
        //   Execute ecall and ebreak
        //   Should cause an exception
        /////////////////////////////////

        """)

    body_lines += f"\n{covergroup}_{coverpoint}_ecall:\n"
    body_lines += "\tli t0, 1               # success code\n"
    body_lines += "\tecall                  # test ecall instruction\n"
    body_lines += "\tli t0, -1              # trap handler skips following instruction so this should not be executed\n"
    addSignature("t0", f"{coverpoint}/{coverpoint}/ecall")

    body_lines += f"\n{covergroup}_{coverpoint}_ebreak:\n"
    body_lines += "\tli t0, 1               # success code\n"
    body_lines += "\tebreak                 # test ebreak instruction\n"
    body_lines += "\tli t0, -1              # trap handler skips following instruction so this should not be executed\n"
    addSignature("t0", f"{coverpoint}/{coverpoint}/ebreak")

    coverpoint = "cp_mret"

    body_lines += dedent("""
        /////////////////////////////////
        // cp_mret
        //   Execute mret while sweeping cross-product of mpp, mprv, mpie, mie
        /////////////////////////////////

            CSRR(s0, mstatus)        # read and save mstatus
            # set up a1 with mstatus except MPP, MPRV, MPIE, MIE cleared
            LI(t0,0x21888)          # t0 has all MPP, MPRV, MPIE, MIE bits set (bits [12:11], [17], [7], [3], respectively)
            not t0, t0              # t0 has all but MPP, MPRV, MPIE, MIE bits set
            and a1, s0, t0          # clear MPP, MPRV, MPIE, MIE bits
        """)

    for mpp in (3,):  # only M-mode; this will expand in other tests
        for mprv in (0, 1):
            for mpie in (0, 1):
                for mie in (0, 1):
                    binname = f"mpp_{mpp:02b}_mprv_{mprv}_mpie_{mpie}_mie_{mie}"
                    body_lines += f"\n{covergroup}_{coverpoint}_{binname}:\n"
                    fields = (mpp << 11) | (mprv << 17) | (mpie << 7) | (mie << 3)
                    body_lines += (
                        f"\tLI(t0, 0x{fields:08x})  # mpp = {mpp:02b} mprv = {mprv} mpie = {mpie} mie = {mie}\n"
                    )
                    body_lines += (
                        "\tor t0, t0, a1          # value to write to mstatus with MPP/MPRV/MPIE/MIE bits set/clear\n"
                    )
                    body_lines += "\tLA(t1, 1f)             # return address after mret\n"
                    body_lines += "\tCSRW(mepc, t1)          # set mepc to return address\n"
                    body_lines += "\tCSRW(mstatus, t0)       # write mstatus with MPP/MPRV/MPIE/MIE bits set/clear\n"
                    body_lines += "\tmret                   # test mret instruction\n"
                    body_lines += "\tli t0, -1              # should not be executed\n"
                    body_lines += "1:                         # mret should return to here\n"
                    addSignature("t0", f"{coverpoint}/{coverpoint}/{binname}_mstatus_wval")
                    # body_lines += "\tRVTEST_GOTO_MMODE      # make sure we return to machine mode\n"
                    addCSRReadTest("mstatus", "t0", f"{coverpoint}/{coverpoint}/{binname}_mstatus_rval")
    body_lines += "\n\tCSRW(mstatus, s0)    # restore CSR\n"

    coverpoint = "cp_sret"

    body_lines += dedent("""
        /////////////////////////////////
        // cp_sret
        //   Execute sret while sweeping cross-product of mprv, spp, spie, sie, tsr
        //   If S-mode is not implemented, sret should raise an illegal instruction exception
        //   Otherwise, go to S or U mode depending on SPP.  SIE <- SPIE.  SPIE <- 1.  MPRV <- 0. SPP <- 0 (U-mode).  TSR has no effect.
        /////////////////////////////////

            CSRR(s0, mstatus)        # read and save mstatus
            # set up a1 with mstatus except MPRV, SPP, SPIE, SIE, TSR cleared
            LI(t0,0x420122)          # t0 has all MPRV, SPP, SPIE, SIE, TSR bits set (bits [17], [8], [5], [1], [22] respectively)
            not t0, t0              # t0 has all but MPRV, SPP, SPIE, SIE, TSR bits set
            and a1, s0, t0          # clear MPRV, SPP, SPIE, SIE, TSR bits
        """)

    for spp in (0, 1):
        for mprv in (0, 1):
            for spie in (0, 1):
                for sie in (0, 1):
                    for tsr in (0, 1):
                        binname = f"spp_{spp}_mprv_{mprv}_spie_{spie}_sie_{sie}_tsr_{tsr}"
                        body_lines += f"\n{covergroup}_{coverpoint}_{binname}:\n"
                        fields = (mprv << 17) | (spp << 8) | (spie << 5) | (sie << 1) | (tsr << 22)
                        body_lines += f"\tLI(t0, 0x{fields:08x}) # mprv = {mprv} spp = {spp} spie = {spie} sie = {sie} tsr = {tsr}\n"
                        body_lines += "\tor t0, t0, a1          # value to write to mstatus with MPRV/SPP/SPIE/SIE/TSR bits set/clear\n"
                        body_lines += "\tLA(t1, 1f)             # return address after sret\n"
                        body_lines += "\tCSRW(sepc, t1)          # set sepc to return address.  Note that sepc might not exist if S-mode is not implemented, and this test will break if writing it hangs\n"
                        body_lines += (
                            "\tCSRW(mstatus, t0)       # write mstatus with MPRV/SPP/SPIE/SIE/TSR bits set/clear\n"
                        )
                        body_lines += "\tsret                   # test sret instruction\n"
                        body_lines += "\tli t0, -1              # should not be executed\n"
                        body_lines += "1:                         # sret should return to here\n"
                        addSignature("t0", f"{coverpoint}/{coverpoint}/{binname}_mstatus_wval")
                        body_lines += "\tRVTEST_GOTO_MMODE      # make sure we return to machine mode\n"
                        addCSRReadTest("mstatus", "t0", f"{coverpoint}/{coverpoint}/{binname}_mstatus_rval")
    body_lines += "\n\tCSRW(mstatus, s0)    # restore CSR\n"

    covergroup = "Sm_mcsr_cg"
    coverpoint = "cp_mcsr_access"

    csrs = [
        "mstatus",
        "misa",
        "medeleg",
        "mideleg",
        "mie",
        "mtvec",
        "mscratch",
        "mepc",
        "mcause",
        "mtval",
        "mip",
        "menvcfg",
    ]
    csrs32 = ["mstatush", "menvcfgh"]  # medelegh only in Sm1p13
    csrsro = ["mvendorid", "mimpid", "marchid", "mhartid", "mconfigptr"]

    body_lines += dedent("""
        /////////////////////////////////
        // cp_mcsr_access
        //   Read, write all 1s, write all 0s, set all 1s, set all 0s, restore all M-mode CSRs
        /////////////////////////////////
        """)

    for csr in csrs:
        addAccessTest(csr, covergroup, coverpoint)
    body_lines += "\n#ifdef MSECCFG_SUPPORTED\n"  # update when this gets a UDB name or depends on other extensions (Zkr, Zmepmp, Smmpmp, Zicflip)
    addAccessTest("mseccfg", covergroup, coverpoint)
    body_lines += "#endif\n"
    body_lines += "\n// Read-Only CSRs\n"
    for csr in csrsro:
        addAccessTest(csr, covergroup, coverpoint)
    body_lines += "\n// RV32-only h CSRs\n"
    body_lines += "#if __riscv_xlen == 32\n"
    for csr in csrs32:
        addAccessTest(csr, covergroup, coverpoint)
    body_lines += "\n#ifdef MSECCFG_SUPPORTED\n"  # update as above
    addAccessTest("mseccfgh", covergroup, coverpoint)
    body_lines += "#endif\n"
    body_lines += "#endif\n"

    coverpoint = "cp_mcsrwalk"

    body_lines += dedent("""
        /////////////////////////////////
        // cp_mcsrwalk
        //   Set and clear each bit individually in all writable M-mode CSRs
        /////////////////////////////////
        """)

    for csr in csrs:
        addWalkTest(csr, covergroup, coverpoint)
    body_lines += "// RV32-only h CSRs\n"
    body_lines += "#if __riscv_xlen == 32\n"
    for csr in csrs32:
        addWalkTest(csr, covergroup, coverpoint)
    body_lines += "#endif\n"

    coverpoint = "cp_csr_insufficient_priv"

    body_lines += dedent("""
        /////////////////////////////////
        // cp_csr_insufficient_priv
        //   Attempt to read debug-mode registers.  Should throw illegal instruction
        /////////////////////////////////

        """)

    for csr in range(0x7B0, 0x7C0):
        body_lines += (
            f"\tCSRR(t0, 0x{csr:03x})    # attempt to read debug-mode CSR {csr:03x}; should get illegal instruction\n"
        )

    coverpoint = "cp_csr_ro"

    body_lines += dedent("""
        /////////////////////////////////
        // cp_csr_ro
        //   Attempt to write read-only CSRs.  Should throw illegal instruction
        /////////////////////////////////

        """)

    body_lines += "\tLI(t0, -1)          # t0 = all 1s\n"
    for csr in range(0xC00, 0x1000):
        body_lines += (
            f"\tCSRW(0x{csr:03x}, t0)   # attempt to write read-only CSR {csr:03x}; should get illegal instruction\n"
        )


##################################
# Global variables
##################################
sigupd_count = 0  # keep track of size of signature
body_lines = ""  # body of test
testcase_lines = (
    '# testcase strings for reporting mismatches\ncanary_mismatch: .string "Testcase signature canary mismatch!"\n'
)

##################################
# Main Function
##################################

basename = "Sm"
fname = f"{basename}-01.S"
pathname = Path(fname)
with Path.open(pathname, "w") as outfile:
    sys.stdout = outfile

    makePrivBody()  # populate body_lines and testcase_lines
    header_lines = makePrivHeader(fname, sigupd_count, "", "")
    footer_lines = makePrivFooter(basename, testcase_lines)

    print(header_lines)
    print(body_lines)
    print(footer_lines)
