from textwrap import dedent

def makePrivHeader(testname: str, sigupd_count: int, extra_reqext: str, extra_march: str) -> str:
    """Generate the header for privileged tests."""
    cmd = " ".join(sys.argv)
    more_reqext = ""
    if extra_reqext:
        more_reqext = ", " + extra_reqext
    more_march = ""
    if extra_march:
        more_march = "_" + extra_march
    # TODO: it would be nice to use e as the base for priv tests to ensure they run everywhere, but Makefiles would need to be updated to emit mabi=ilp32e/lp64e
    march_base = "rv${XLEN}i_zicsr"
    header_lines = dedent(f"""\
        ///////////////////////////////////////////
        // {testname}.S
        //
        // Autogenerated by {cmd}
        // Do not modify manually
        //
        // SPDX-License-Identifier: Apache-2.0
        ///////////////////////////////////////////

        ##### START_TEST_CONFIG #####
        # REQUIRED_EXTENSIONS: [I, Zicsr, Sm{more_reqext}]
        # MARCH: {march_base}{more_march}
        # CONFIG_DEPENDENT: true
        ##### END_TEST_CONFIG #####

        #define TEST_FILE "{fname}"
        #define SIGUPD_COUNT {sigupd_count}  // figure out count
        #include "riscv_arch_test.h"
        .option norvc          # Do not automatically replace uncompressed instructions with compressed instructions

        // Set up test environment and declare main entry point
        RVTEST_BEGIN

        """)
    return header_lines


def makePrivFooter(basename: str, testcase_lines: str) -> str:
    """Generate the footer for privileged tests."""
    footer_lines = dedent(f"""\
        // Instantiate trap handlers and other epilog code, call RVMODEL_HALT at end of test
        RVTEST_CODE_END

        // Create scratch data sections and label for test data
        RVTEST_DATA_BEGIN

        // Testcase strings
        canary_mismatch: .string "Testcase signature canary mismatch!"
        {testcase_lines}
        #test_1: .string "\"test: 1; cp: {basename}/cp_rs1\""

        // End of test data section and model specific data
        RVTEST_DATA_END

        // Signature region for self-checking
        RVTEST_SIG_SETUP
        """)
    return footer_lines

def addCSRWriteTest(csr_name: str, reg: str, coverpoint: str, bin: str):
    """Add a CSR write test.  Update global variables, including the signature update count, body lines, and testcase lines."""
    global sigupd_count, body_lines, testcase_lines
    sigupd_count += 1
    body_lines += f"\tCSR_WRITE({csr_name}, {reg}, {reg}, DEFAULT_SIG_PTR, DEFAULT_LINK_REG, DEFAULT_TEMP_REG, test_{sigupd_count})\n"
    testcase_lines += f'test_{sigupd_count}: .string "\\"test: {sigupd_count}; cp: {coverpoint}_{bin}\\""\n'


